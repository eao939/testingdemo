#' Adds two numbers
#'
#' @param a 
#' @param b 
#'
#' @returns the sum of a + b
#' @export
#'
#' @examples
#' add_numbers(2,3)
add_numbers <- function(a,b) {
  return(a+b)
}



#' Multiplies two numbers
#'
#' @param a 
#' @param b 
#'
#' @returns the product of a and b
#' @export
#'
#' @examples
#' add_numbers(2,3)
product_numbers <- function(a,b) {
  return(a*b)
}